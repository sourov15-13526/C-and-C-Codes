#include<stdio.h>
int main()
{
    int var1 = 5;
    char var2 = 'a';
    float var3 = 6.24;
    double var4 = 50.25136;
    printf("Enter the integer value = %d\n",var1);
    printf("Enter the character value = %c\n",var2);
    printf("Enter the floating value = %f\n",var3);
    printf("Enter the double value = %lf\n",var4);
    return 0;
}
